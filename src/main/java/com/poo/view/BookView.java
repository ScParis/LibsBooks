    private void fillTable(List<Book> books) {
        
        DefaultTableModel model = new DefaultTableModel();
        tbBooks.setModel(model);
        
        model.addColumn("ID");
        model.addColumn("Nome");
        tbBooks.getColumnModel().getColumn(0).setPreferredWidth(10);
        
        for (Book book : books) {
            model.addRow(new Object[]{book.getId(), book.getTitle()});
        }
    }



